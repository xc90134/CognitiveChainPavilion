package org.chainpavilion.model.enums;

/**
 * 资源分类枚举
 * 
 * 定义系统中资源的分类类型：
 * - COURSE: 课程教程
 * - ARTICLE: 文章资讯
 * - CODE: 代码示例
 * - TOOL: 开发工具
 * - OTHER: 其他资源
 * 
 * @author 知链智阁团队
 */
public enum ResourceCategory {
    COURSE,    // 课程教程
    ARTICLE,   // 文章资讯
    CODE,      // 代码示例
    TOOL,      // 开发工具
    OTHER      // 其他资源
} 